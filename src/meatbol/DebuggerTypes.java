package meatbol;

/** DebuggerTypes hold String constants that represent
 * the way the debugger types are represented in Meatbol.
 * <p>
 * Each of the constants stored in this class represent a
 * debugger option for Meatbol.
 *
 * @author Mason Pohler
 */
public class DebuggerTypes {
    /**
     * String literal for the Token debugger option.
     */
    public static final String TOKEN = "Token";

    /**
     * String literal for the Expression debugger option.
     */
    public static final String EXPRESSION = "Expr";

    /**
     * String literal for the Assignment debugger option.
     */
    public static final String ASSIGNMENT = "Assign";

    /**
     * String literal for the Statement debugger option.
     */
    public static final String STATEMENT = "Stmt";
}
